/*
A robot is located at the top-left corner of a m x n grid (marked 'Start' in the diagram below).

![图片](https://assets.leetcode.com/uploads/2018/10/22/robot_maze.png)

The robot can only move either down or right at any point in time. The robot is trying to reach the bottom-right corner of the grid (marked 'Finish' in the diagram below).

Now consider if some obstacles are added to the grids. How many unique paths would there be?



An obstacle and empty space is marked as 1 and 0 respectively in the grid.

Note: m and n will be at most 100.

Example 1:

Input:
[
  [0,0,0],
  [0,1,0],
  [0,0,0]
]
Output: 2
Explanation:
There is one obstacle in the middle of the 3x3 grid above.
There are two ways to reach the bottom-right corner:
1. Right -> Right -> Down -> Down
2. Down -> Down -> Right -> Right
*/

func uniquePathsWithObstacles(obstacleGrid [][]int) int {
	rows := len(obstacleGrid)
	if rows == 0 {
		return 0
	}
	cols := len(obstacleGrid[0])

	steps := make([][]int, rows)
	for i := 0; i < rows; i++ {
		steps[i] = make([]int, cols)
	}

	// 设置初始位置
	if cols > 0 && obstacleGrid[0][0] == 0 {
		steps[0][0] = 1
	}

	for i := 0; i < rows; i++ {
		for j := 0; j < cols; j++ {
			current := obstacleGrid[i][j]
			if current == 1 || (i == 0 && j == 0) {
				continue
			}
			if i == 0 {
				steps[i][j] = steps[0][j] + steps[i][j-1]
			} else if j == 0 {
				steps[i][j] = steps[i-1][j] + steps[i][0]
			} else {
				if i == 21 && j == 28 {
					fmt.Printf("%d,%d\n", steps[i-1][j], steps[i][j-1])
					fmt.Printf("%d\n", steps[i-1][j]+steps[i][j-1])
				}
				steps[i][j] = steps[i-1][j] + steps[i][j-1]
			}
		}
	}

	return steps[rows-1][cols-1]
}

